<template>
  <section>
    <header>
      <h3>My Friends</h3>
    </header>
    <ul>
      <friend-contact
        v-for="friend in friends"
        :key="friend.id"
        :friend="friend"
        @toggle-favorite="toggleFavorite"
      />
    </ul>
    >
  </section>
</template>

<script lang="ts">
export interface FriendPropOptions {
  [property: string]: string;
}
export default {
  data() {
    return {
      friends: [
        {
          id: "manual",
          name: "Manual Lorenz",
          phone: "01234 5678 991",
          email: "manual@localhost.com",
          isFavorite: true,
        },
        {
          id: "julie",
          name: "Julie Jones",
          phone: "0987 6543 221",
          email: "julie@localhost.com",
          isFavorite: false,
        },
      ],
    };
  },
  methods: {
    toggleFavorite(friendId: string) {
      const identifyFriend = (this as any).friends.find(
        (friend: FriendPropOptions) => friend.id === friendId
      );
      identifyFriend.isFavorite = !identifyFriend.isFavorite;
    },
  },
};
</script>

